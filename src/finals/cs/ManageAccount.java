    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String student_number = txtSnumber.getText();

        int result = JOptionPane.showConfirmDialog(this,"Are you sure you want to delete the selected record?", "DELETE RECORD",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(result == JOptionPane.YES_OPTION){

            try{
                String  query = "DELETE FROM `student_account` WHERE `student_number` = '"+student_number+"'";
                statement = con.prepareStatement(query);
                statement.execute();

                JOptionPane.showMessageDialog(this, "Message has been successfully deleted!","INFORMATION", JOptionPane.OK_OPTION);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,"Error in deleting... "+e);
            }

        }else if (result == JOptionPane.NO_OPTION){

        }

        fillListView();

        txtFirstname.setText("");
        txtLastname.setText("");
        txtEmail.setText("");
        txtPassword.setText("");
        txtSnumber.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(btnSave.getText().equals("Save"))
        {
            if(txtFirstname.getText().isEmpty() || txtLastname.getText().isEmpty() || txtEmail.getText().isEmpty()
                || txtPassword.getText().isEmpty() || txtSnumber.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Complete all fields with correct entries!",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                String first_name, last_name, email, password, student_number;

                first_name = txtFirstname.getText();
                last_name = txtLastname.getText();
                email = txtEmail.getText();
                password = txtPassword.getText();
                student_number = txtSnumber.getText();


                try{

                    String query = "UPDATE `student_account` SET `first_name`='"+first_name+"',`last_name`='"+last_name+"',`email`='"+email+"',`password`='"+password+"',`student_number`='"+student_number+"' WHERE `student_number` = '"+student_number+"''";
                    statement = con.prepareStatement(query);
                    statement.executeUpdate();

                    JOptionPane.showMessageDialog(this, "Record Has Been Successfully Saved!","INFORMATION",JOptionPane.INFORMATION_MESSAGE);

                    fillListView();

                   txtFirstname.setText("");
                    txtLastname.setText("");
                    txtEmail.setText("");
                    txtPassword.setText("");
                    txtSnumber.setText("");

                    txtFirstname.setEnabled(false);
                    //txtFirstname.setVisible(true);
                    txtLastname.setEnabled(false);
                    //txtLastname.setVisible(true);
                    txtEmail.setEnabled(false);
                    //txtEmail.setVisible(true);
                    txtPassword.setEnabled(false);
                    //txtPassword.setVisible(true);
                    txtSnumber.setEnabled(false);
                    //txtSnumber.setVisible(true);

                    btnEdit.setEnabled(false);
                    btnDelete.setEnabled(false);
                    btnSave.setEnabled(true);
                    btnCancel.setEnabled(true);
                    btnCancel.setText("Close");

                }catch(Exception e){
                    System.out.println(e.getMessage());
                }
            }
        } 
    }//GEN-LAST:event_btnSaveActionPerformed

