private void fillListView() {
        try{
            String  query = "SELECT * FROM `student_account`";
            statement = con.prepareStatement(query);
            result = statement.executeQuery();
            DefaultTableModel tm = (DefaultTableModel)tblRecord.getModel();
            tm.setRowCount(0);

            while(result.next()){
                Object o[] = {result.getString("first_name"),result.getString("last_name"),result.getString("email"),result.getString("password"),result.getString("student_number")};
                tm.addRow(o);
                }
            }
            catch(Exception e){

                JOptionPane.showMessageDialog(null,"No Record..."+e);
            }
    }

