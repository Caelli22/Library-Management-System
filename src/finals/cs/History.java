    private void fillListView() {
        try{
            String  query = "SELECT * FROM `history`";
            statement = con.prepareStatement(query);
            result = statement.executeQuery();
            DefaultTableModel tm = (DefaultTableModel)tblHistory.getModel();
            tm.setRowCount(0);

            while(result.next()){
                Object o[] = {result.getString("status"),result.getString("name"),result.getString("book"),result.getString("time"),result.getString("date")};
                tm.addRow(o);
                }
            }
            catch(Exception e){

                JOptionPane.showMessageDialog(null,"No Record..."+e);
            }
    }
